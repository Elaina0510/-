#include<iostream>
using namespace std;

int main() {

	//И│охн╦╦сич

	// =
	int a = 10;
	a = 16;
	cout << "a=" << a << endl;

	// +=
	a += 2;  // a = a + 2
	cout << "a=" << a << endl;

	// -=
	a = 30;
	a -= 2; // a = a - 30
	cout << "a=" << a << endl;

	// *=
	a *= 2;
	cout << "a=" << a << endl;

	// /=
	a /= 3;
	cout << "a=" << a << endl;

	// %=
	a %= 2; // a= a % 2
	cout << "a=" << a << endl;



	system("pause");

	return 0;
}